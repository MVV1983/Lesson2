        super.onViewCreated(view, savedInstanceState)

        view.findViewById<Button>(R.id.goto_firstFragmentBtn)?.setOnClickListener(Navigation.createNavigateOnClickListener(R.id.action_homeFragment_to_firstFragment))
    }


}